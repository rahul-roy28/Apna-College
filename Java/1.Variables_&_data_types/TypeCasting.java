import java.util.*;

public class TypeCasting{
  public static void main(String args[]){
  Scanner sc=new Scanner(System.in);
  //char to int casting
  char ch='P';
  int no=ch;
  System.out.println(no);
  char ch1='u';
  int no1=ch1;
  System.out.println(no1);
  char ch2='j';
  int no2=ch2;
  System.out.println(no2);
  char ch3='a';
  int no3=ch3;
  System.out.println(no3);
  //float to int
  float a=22.56f;
  int b=(int)a;
  System.out.println(b);
  sc.close();
 }
}